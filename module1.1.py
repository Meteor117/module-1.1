example = 'Топинамбур'
print(example[0])#первый символ строки
print(example[-1])#последний символ строки (используя отрицательный индекс)
print(example[5::])#вторая половина строки (С нечётным количеством символов)
print(example[::-1])#слово наоборот
print(example[1:10:2])#каждый второй символ строки
